  Future<void> getData() async {
    setState(() {
      pictureRecorder = new PictureRecorder();
      canvas = Canvas(pictureRecorder);
      textPainter = TextPainter(textDirection: TextDirection.ltr);
      iconStr = String.fromCharCode(iconData.codePoint);
      textPainter.text = TextSpan(
          text: iconStr,
          style: TextStyle(
            letterSpacing: 0.0,
            fontSize: 60.0,
            fontFamily: iconData.fontFamily,
            color: Colors.greenAccent,
          )
      );
      textPainter.layout();
      textPainter.paint(canvas, Offset(0.0, 0.0));
    });
    final picture = pictureRecorder.endRecording();
    final image = await picture.toImage(60, 60);
    final bytes = await image.toByteData(format: ImageByteFormat.png);
    setState(() {
      Bit =
          BitmapDescriptor.fromBytes(bytes.buffer.asUint8List());
    });


    rootBundle.loadString('assets/map_style.txt').then((string) {
      _mapStyle = string;
    });
    Position ps = await Geolocator().getCurrentPosition();
    setState(() {
      currentLocation = new LatLng(ps.latitude, ps.longitude);
    });

    dnsnMarker.forEach((element) {
      setState(() {
        dnsnToShow.add(
          element.copyWith(iconParam: Bit,infoWindowParam: InfoWindow(title: element.markerId.value,),),
        );
      });
    });
  }

  @override
  void initState() {
    getData();
    super.initState();
  }
